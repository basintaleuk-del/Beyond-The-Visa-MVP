create or replace function public.admin_set_user_access(p_user uuid,p_plan text,p_status text,p_reason text default null)
returns void language plpgsql security definer set search_path=public as $$
begin
 if not public.btv_is_admin() then raise exception 'Administrator access required'; end if;
 if p_plan not in ('free','premium') or p_status not in ('active','suspended') then raise exception 'Invalid access value'; end if;
 update public.profiles set account_type=p_plan,updated_at=now() where id=p_user and role<>'admin';
 insert into public.user_status(user_id,status,reason,suspended_at,suspended_by,updated_at)
 values(p_user,p_status,p_reason,case when p_status='suspended' then now() end,case when p_status='suspended' then auth.uid() end,now())
 on conflict(user_id) do update set status=excluded.status,reason=excluded.reason,suspended_at=excluded.suspended_at,suspended_by=excluded.suspended_by,updated_at=now();
 insert into public.admin_audit_logs(admin_id,action,target_type,target_id,details)
 values(auth.uid(),'set_user_access','user',p_user::text,jsonb_build_object('plan',p_plan,'status',p_status,'reason',p_reason));
end $$;

create or replace function public.admin_list_users(p_search text default '',p_plan text default null,p_status text default null,p_limit int default 50,p_offset int default 0)
returns table(id uuid,email text,full_name text,profession text,destination text,account_type text,role text,status text,created_at timestamptz,last_sign_in_at timestamptz)
language sql security definer set search_path=public,auth as $$
 select p.id,u.email,p.full_name,p.profession,p.destination,p.account_type,p.role,coalesce(s.status,'active'),p.created_at,u.last_sign_in_at
 from public.profiles p join auth.users u on u.id=p.id left join public.user_status s on s.user_id=p.id
 where public.btv_is_admin()
 and (p_search='' or coalesce(p.full_name,'') ilike '%'||p_search||'%' or coalesce(u.email,'') ilike '%'||p_search||'%')
 and (p_plan is null or p.account_type=p_plan) and (p_status is null or coalesce(s.status,'active')=p_status)
 order by p.created_at desc limit least(greatest(p_limit,1),200) offset greatest(p_offset,0)
$$;
revoke all on function public.admin_list_users(text,text,text,int,int) from public;
grant execute on function public.admin_list_users(text,text,text,int,int) to authenticated;

create or replace function public.btv_audit_admin_change()
returns trigger language plpgsql security definer set search_path=public as $$
declare row_data jsonb;
begin
 row_data:=case when tg_op='DELETE' then to_jsonb(old) else to_jsonb(new) end;
 if auth.uid() is not null and public.btv_is_admin() then
  insert into public.admin_audit_logs(admin_id,action,target_type,target_id,details)
  values(auth.uid(),lower(tg_op),tg_table_name,coalesce(row_data->>'id',row_data->>'user_id'),jsonb_build_object('operation',tg_op));
 end if;if tg_op='DELETE' then return old;end if;return new;
end $$;
do $$ declare t text; begin
 foreach t in array array['content_categories','articles','learning_resources','announcements','reported_questions','cbt_questions','nclex_questions'] loop
  execute format('drop trigger if exists btv_admin_audit on public.%I',t);
  execute format('create trigger btv_admin_audit after insert or update or delete on public.%I for each row execute function public.btv_audit_admin_change()',t);
 end loop;
end $$;

