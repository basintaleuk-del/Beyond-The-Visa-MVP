-- 001: CMS, account status and auditable administration (additive)
create extension if not exists pgcrypto;

create or replace function public.btv_is_admin()
returns boolean language sql stable security definer set search_path=public
as $$ select exists(select 1 from public.profiles where id=auth.uid() and role='admin') $$;

create or replace function public.btv_is_premium()
returns boolean language sql stable security definer set search_path=public
as $$ select public.btv_is_admin() or exists(select 1 from public.profiles where id=auth.uid() and account_type='premium') $$;

create table if not exists public.user_status (
  user_id uuid primary key references auth.users(id) on delete cascade,
  status text not null default 'active' check(status in ('active','suspended')),
  reason text,
  suspended_at timestamptz,
  suspended_by uuid references auth.users(id),
  updated_at timestamptz not null default now()
);
create table if not exists public.admin_audit_logs (
  id bigint generated by default as identity primary key,
  admin_id uuid not null references auth.users(id), action text not null,
  target_type text not null, target_id text, details jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now()
);
create table if not exists public.content_categories (
  id uuid primary key default gen_random_uuid(), name text not null,
  slug text not null unique, content_type text not null,
  description text, is_active boolean not null default true,
  created_at timestamptz not null default now()
);
create table if not exists public.articles (
  id uuid primary key default gen_random_uuid(), title text not null,
  slug text not null unique, summary text not null default '', body text not null,
  country text, profession text, category text, featured_image_url text,
  access_level text not null default 'free' check(access_level in ('free','premium')),
  status text not null default 'draft' check(status in ('draft','published','archived')),
  author_id uuid references auth.users(id), published_at timestamptz,
  view_count bigint not null default 0, created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);
create table if not exists public.learning_resources (
  id uuid primary key default gen_random_uuid(), title text not null,
  description text not null default '', resource_type text not null check(resource_type in ('video','pdf','link','document')),
  country text, exam text, category text, thumbnail_url text, external_url text,
  storage_path text, mime_type text, size_bytes bigint,
  access_level text not null default 'free' check(access_level in ('free','premium')),
  status text not null default 'draft' check(status in ('draft','published','archived')),
  created_by uuid references auth.users(id), created_at timestamptz not null default now(), updated_at timestamptz not null default now(),
  check(external_url is not null or storage_path is not null)
);
create table if not exists public.announcements (
  id uuid primary key default gen_random_uuid(), title text not null, body text not null,
  target_type text not null default 'all' check(target_type in ('all','premium','country','exam','users')),
  target_value text, target_user_ids uuid[] not null default '{}',
  status text not null default 'draft' check(status in ('draft','scheduled','published','archived')),
  scheduled_at timestamptz, published_at timestamptz, created_by uuid references auth.users(id),
  created_at timestamptz not null default now(), updated_at timestamptz not null default now()
);
create table if not exists public.announcement_reads (
  announcement_id uuid references public.announcements(id) on delete cascade,
  user_id uuid references auth.users(id) on delete cascade,
  read_at timestamptz not null default now(), primary key(announcement_id,user_id)
);
create table if not exists public.reported_questions (
  id bigint generated by default as identity primary key,
  user_id uuid references auth.users(id) on delete set null,
  exam_type text not null check(exam_type in ('cbt','nclex')), question_id bigint not null,
  reason text not null, details text,
  status text not null default 'open' check(status in ('open','reviewing','resolved','dismissed')),
  reviewed_by uuid references auth.users(id), reviewed_at timestamptz,
  created_at timestamptz not null default now(), unique(user_id,exam_type,question_id,reason)
);

