-- 004: transactional booking system
create extension if not exists btree_gist;
create table if not exists public.booking_services (
 id uuid primary key default gen_random_uuid(), name text not null, slug text not null unique,
 description text not null default '', duration_minutes int not null check(duration_minutes between 10 and 480),
 buffer_minutes int not null default 0 check(buffer_minutes between 0 and 180),
 price_minor int not null default 0 check(price_minor>=0), currency text not null default 'GHS',
 cancellation_hours int not null default 24, reschedule_hours int not null default 24,
 max_bookings_per_slot int not null default 1 check(max_bookings_per_slot between 1 and 100),
 is_active boolean not null default true, created_at timestamptz not null default now(), updated_at timestamptz not null default now()
);
create table if not exists public.availability_rules (
 id uuid primary key default gen_random_uuid(), service_id uuid references public.booking_services(id) on delete cascade,
 weekday int not null check(weekday between 0 and 6), start_time time not null, end_time time not null,
 timezone text not null default 'Europe/London', valid_from date, valid_until date, is_active boolean not null default true,
 check(start_time<end_time)
);
create table if not exists public.blocked_dates (
 id uuid primary key default gen_random_uuid(), service_id uuid references public.booking_services(id) on delete cascade,
 starts_at timestamptz not null, ends_at timestamptz not null, reason text, check(starts_at<ends_at)
);
create table if not exists public.bookings (
 id uuid primary key default gen_random_uuid(), user_id uuid not null references auth.users(id) on delete cascade,
 service_id uuid not null references public.booking_services(id), starts_at timestamptz not null, ends_at timestamptz not null,
 customer_name text not null, customer_email text not null, role_band text, notes text,
 status text not null default 'pending_payment' check(status in ('pending_payment','confirmed','cancelled','completed','no_show')),
 payment_status text not null default 'not_required' check(payment_status in ('not_required','pending','paid','failed','refunded')),
 meeting_provider text, meeting_url text, admin_notes text,
 created_at timestamptz not null default now(), updated_at timestamptz not null default now(), check(starts_at<ends_at)
);
create table if not exists public.booking_payments (
 id uuid primary key default gen_random_uuid(), booking_id uuid not null references public.bookings(id) on delete cascade,
 paystack_reference text not null unique, amount_minor int not null, currency text not null,
 status text not null default 'pending' check(status in ('pending','successful','failed','refunded')),
 verified_at timestamptz, provider_payload jsonb not null default '{}', created_at timestamptz not null default now()
);
create table if not exists public.booking_status_history (
 id bigint generated by default as identity primary key, booking_id uuid not null references public.bookings(id) on delete cascade,
 from_status text, to_status text not null, changed_by uuid references auth.users(id), note text, created_at timestamptz not null default now()
);
create unique index if not exists bookings_active_slot_uidx on public.bookings(service_id,starts_at) where status in ('pending_payment','confirmed');
