create or replace function public.create_booking(p_service uuid,p_starts timestamptz,p_name text,p_email text,p_role text default null,p_notes text default null)
returns uuid language plpgsql security definer set search_path=public as $$
declare s public.booking_services; new_id uuid; finish timestamptz; blocked boolean; available boolean;
begin
 if auth.uid() is null then raise exception 'Sign in required'; end if;
 select * into s from public.booking_services where id=p_service and is_active for share;
 if not found then raise exception 'Service unavailable'; end if;
 if p_starts<=now() then raise exception 'Choose a future time'; end if;
 finish:=p_starts+make_interval(mins=>s.duration_minutes);
 select exists(select 1 from public.availability_rules r where r.service_id=p_service and r.is_active
   and extract(dow from (p_starts at time zone r.timezone))::int=r.weekday
   and (r.valid_from is null or (p_starts at time zone r.timezone)::date>=r.valid_from)
   and (r.valid_until is null or (p_starts at time zone r.timezone)::date<=r.valid_until)
   and (p_starts at time zone r.timezone)::time>=r.start_time
   and (finish at time zone r.timezone)::time<=r.end_time) into available;
 if not available then raise exception 'Choose a time within the published availability.'; end if;
 select exists(select 1 from public.blocked_dates d where (d.service_id is null or d.service_id=p_service) and tstzrange(d.starts_at,d.ends_at,'[)') && tstzrange(p_starts,finish,'[)')) into blocked;
 if blocked then raise exception 'This time is unavailable'; end if;
 begin
  insert into public.bookings(user_id,service_id,starts_at,ends_at,customer_name,customer_email,role_band,notes,status,payment_status)
  values(auth.uid(),p_service,p_starts,finish,trim(p_name),lower(trim(p_email)),p_role,p_notes,case when s.price_minor=0 then 'confirmed' else 'pending_payment' end,case when s.price_minor=0 then 'not_required' else 'pending' end)
  returning id into new_id;
 exception when unique_violation then raise exception 'That slot was just booked. Please choose another.';
 end;
 insert into public.booking_status_history(booking_id,to_status,changed_by,note) values(new_id,case when s.price_minor=0 then 'confirmed' else 'pending_payment' end,auth.uid(),'Booking created');
 if s.price_minor=0 then insert into public.notifications(user_id,category,title,body,action_url) values(auth.uid(),'bookings','Booking confirmed','Your free booking has been confirmed.','#bookingCentre'); end if;
 return new_id;
end $$;

create or replace function public.manage_own_booking(p_booking uuid,p_action text,p_new_start timestamptz default null)
returns void language plpgsql security definer set search_path=public as $$
declare b public.bookings;s public.booking_services;new_end timestamptz;
begin
 select * into b from public.bookings where id=p_booking and user_id=auth.uid() for update;
 if not found then raise exception 'Booking not found'; end if;
 select * into s from public.booking_services where id=b.service_id;
 if b.status not in ('pending_payment','confirmed') then raise exception 'This booking can no longer be changed'; end if;
 if p_action='cancel' then
  if now()>b.starts_at-make_interval(hours=>s.cancellation_hours) then raise exception 'The cancellation deadline has passed'; end if;
  update public.bookings set status='cancelled',updated_at=now() where id=b.id;
  insert into public.booking_status_history(booking_id,from_status,to_status,changed_by,note) values(b.id,b.status,'cancelled',auth.uid(),'Cancelled by user');
 elsif p_action='reschedule' then
  if p_new_start is null or now()>b.starts_at-make_interval(hours=>s.reschedule_hours) then raise exception 'The reschedule deadline has passed'; end if;
  new_end:=p_new_start+make_interval(mins=>s.duration_minutes);
  update public.bookings set starts_at=p_new_start,ends_at=new_end,updated_at=now() where id=b.id;
  insert into public.booking_status_history(booking_id,from_status,to_status,changed_by,note) values(b.id,b.status,b.status,auth.uid(),'Rescheduled by user');
 else raise exception 'Invalid booking action'; end if;
exception when exclusion_violation or unique_violation then raise exception 'That slot is no longer available';
end $$;
